!----------------------------------------------------------------------------------------------------------------------------------

SUBROUTINE TEST_NO_INIT()
    IMPLICIT NONE
    CALL CHECK_MPL_ALLOCATOR_IS_INITIALIZED(.FALSE.)
    IF (MPL_ALLOCATOR_ALLOCATED() /= 0) CALL WARN("Expected: MPL_ALLOCATOR_ALLOCATED() == 0")
    IF (MPL_ALLOCATOR_CAPACITY() == 0 .AND. MPL_ALLOCATOR_CAPACITY() /= -1) CALL WARN("Expected: MPL_ALLOCATOR_CAPACITY() > 0")
    CALL VARIOUS_ALLOCATIONS_WITH_SHAPE
    CALL VARIOUS_ALLOCATIONS_WITH_BOUNDS
    IF (MPL_ALLOCATOR_ALLOCATED() /= 0 ) CALL WARN("Expected: MPL_ALLOCATOR_ALLOCATED() == 0")
    CALL CHECK_MPL_RESOURCE_IS_EXPECTED("FORTRAN")
    CALL CHECK_MPL_ALLOCATOR_IS_INITIALIZED(.FALSE.)
END SUBROUTINE TEST_NO_INIT

!----------------------------------------------------------------------------------------------------------------------------------

SUBROUTINE TEST_INIT_NO_SIZE(RESOURCE)
    IMPLICIT NONE
    CHARACTER(LEN=*), INTENT(IN), OPTIONAL :: RESOURCE
    CALL CHECK_MPL_ALLOCATOR_IS_INITIALIZED(.FALSE.)
    IF (PRESENT(RESOURCE)) THEN
        CALL MPL_ALLOCATOR_INIT(RESOURCE=RESOURCE)
        CALL CHECK_MPL_RESOURCE_IS_EXPECTED(RESOURCE)
    ELSE
        CALL MPL_ALLOCATOR_INIT()
        CALL CHECK_MPL_RESOURCE_IS_EXPECTED()
    ENDIF
    CALL CHECK_MPL_ALLOCATOR_IS_INITIALIZED(.TRUE.)

    IF (MPL_ALLOCATOR_ALLOCATED() /= 0) CALL WARN("Expected: MPL_ALLOCATOR_ALLOCATED() == 0")
    CALL VARIOUS_ALLOCATIONS_WITH_SHAPE
    CALL VARIOUS_ALLOCATIONS_WITH_BOUNDS
    IF (MPL_ALLOCATOR_ALLOCATED() /= 0 ) CALL WARN("Expected: MPL_ALLOCATOR_ALLOCATED() == 0")
    CALL MPL_ALLOCATOR_FINAL()
END SUBROUTINE TEST_INIT_NO_SIZE

!----------------------------------------------------------------------------------------------------------------------------------

SUBROUTINE TEST_INIT_SIZE(RESOURCE)
    IMPLICIT NONE
    CHARACTER(LEN=*), INTENT(IN), OPTIONAL :: RESOURCE
    CALL CHECK_MPL_ALLOCATOR_IS_INITIALIZED(.FALSE.)
    IF (PRESENT(RESOURCE)) THEN
        CALL MPL_ALLOCATOR_INIT(RESOURCE=RESOURCE, SIZE=INT(1024*1024,JPIB))
        CALL CHECK_MPL_RESOURCE_IS_EXPECTED(RESOURCE)
    ELSE
        CALL MPL_ALLOCATOR_INIT(SIZE=INT(1024*1024,JPIB))
        CALL CHECK_MPL_RESOURCE_IS_EXPECTED()
    ENDIF
    CALL CHECK_MPL_ALLOCATOR_IS_INITIALIZED(.TRUE.)
    IF (MPL_ALLOCATOR_ALLOCATED() /= 0) CALL WARN("Expected: MPL_ALLOCATOR_ALLOCATED() == 0")
    IF (MPL_ALLOCATOR_CAPACITY() == 0 .AND. MPL_ALLOCATOR_CAPACITY() /= -1) CALL WARN("Expected: MPL_ALLOCATOR_CAPACITY() > 0")
    CALL VARIOUS_ALLOCATIONS_WITH_SHAPE
    CALL VARIOUS_ALLOCATIONS_WITH_BOUNDS
    IF (MPL_ALLOCATOR_ALLOCATED() /= 0 ) CALL WARN("Expected: MPL_ALLOCATOR_ALLOCATED() == 0")
    CALL MPL_ALLOCATOR_FINAL()
END SUBROUTINE TEST_INIT_SIZE

!----------------------------------------------------------------------------------------------------------------------------------

SUBROUTINE TEST_INIT_AND_RESERVE(RESOURCE)
    IMPLICIT NONE
    CHARACTER(LEN=*), INTENT(IN), OPTIONAL :: RESOURCE
    CALL CHECK_MPL_ALLOCATOR_IS_INITIALIZED(.FALSE.)
    IF (PRESENT(RESOURCE)) THEN
        CALL MPL_ALLOCATOR_INIT(RESOURCE=RESOURCE)
        CALL CHECK_MPL_RESOURCE_IS_EXPECTED(RESOURCE)
    ELSE
        CALL MPL_ALLOCATOR_INIT()
        CALL CHECK_MPL_RESOURCE_IS_EXPECTED()
    ENDIF
    CALL CHECK_MPL_ALLOCATOR_IS_INITIALIZED(.TRUE.)
    IF (MPL_ALLOCATOR_ALLOCATED() /= 0 ) CALL WARN("Expected: MPL_ALLOCATOR_ALLOCATED() == 0")
    IF (MPL_ALLOCATOR_CAPACITY() /= 0 .AND. MPL_ALLOCATOR_CAPACITY() /= -1) CALL WARN("Expected: MPL_ALLOCATOR_CAPACITY() == 0")
    CALL MPL_ALLOCATOR_RESERVE(SIZE=INT(1024*1024,JPIB))
    IF (MPL_ALLOCATOR_ALLOCATED() /= 0 ) CALL WARN("Expected: MPL_ALLOCATOR_ALLOCATED() == 0")
    IF (MPL_ALLOCATOR_CAPACITY() == 0 .AND. MPL_ALLOCATOR_CAPACITY() /= -1) CALL WARN("Expected: MPL_ALLOCATOR_CAPACITY() > 0")
    CALL VARIOUS_ALLOCATIONS_WITH_SHAPE
    CALL VARIOUS_ALLOCATIONS_WITH_BOUNDS
    IF (MPL_ALLOCATOR_ALLOCATED() /= 0 ) CALL WARN("Expected: MPL_ALLOCATOR_ALLOCATED() == 0")
    CALL MPL_ALLOCATOR_FINAL()
END SUBROUTINE TEST_INIT_AND_RESERVE

!----------------------------------------------------------------------------------------------------------------------------------

END MODULE

!----------------------------------------------------------------------------------------------------------------------------------
! This is the driver program that runs the tests in the above module
!----------------------------------------------------------------------------------------------------------------------------------

PROGRAM TEST_MPL_ALLOCATOR_DRIVER
USE TEST_MPL_ALLOCATOR
USE, INTRINSIC :: ISO_FORTRAN_ENV, ONLY : OUTPUT_UNIT
USE MPL_MODULE, ONLY : MPL_INIT, MPL_END, MPL_OUTPUT, MPL_UNIT
IMPLICIT NONE

INTEGER                         :: ARGC, IARG
CHARACTER(LEN=512), ALLOCATABLE :: ARGV(:)
CHARACTER(LEN=512) :: TEST_NAME
ARGC = COMMAND_ARGUMENT_COUNT()
ALLOCATE(ARGV(ARGC))
DO IARG = 1, ARGC
    CALL GET_COMMAND_ARGUMENT(IARG, ARGV(IARG))
END DO

IF (MPL()) THEN
    CALL MPL_INIT(KOUTPUT=1)
ELSE
    MPL_UNIT=OUTPUT_UNIT
    MPL_OUTPUT=1
ENDIF

IF( ARGC > 0 ) THEN
    TEST_NAME = ARGV(1)
    IF (TEST_NAME /= "") THEN
        WRITE(OUTPUT_UNIT,'(A)') "Running test: "//TRIM(TEST_NAME)
        IF (TEST_NAME == "fiat_test_mpl_allocator_no_init") THEN
            CALL TEST_NO_INIT()
        ELSE IF (TEST_NAME == "fiat_test_mpl_allocator_init") THEN
            CALL TEST_INIT_NO_SIZE()
        ELSE IF (TEST_NAME == "fiat_test_mpl_allocator_init_FORTRAN") THEN
            CALL TEST_INIT_NO_SIZE(RESOURCE="FORTRAN")
        ELSE IF (TEST_NAME == "fiat_test_mpl_allocator_init_BUDDY_ALLOC") THEN
            CALL TEST_INIT_NO_SIZE(RESOURCE="BUDDY_ALLOC")
        ELSE IF (TEST_NAME == "fiat_test_mpl_allocator_init_MPI_POOL") THEN
            CALL TEST_INIT_NO_SIZE(RESOURCE="MPI_POOL")
        ELSE IF (TEST_NAME == "fiat_test_mpl_allocator_init_size") THEN
            CALL TEST_INIT_SIZE()
        ELSE IF (TEST_NAME == "fiat_test_mpl_allocator_init_size_FORTRAN") THEN
            CALL TEST_INIT_SIZE(RESOURCE="FORTRAN")
        ELSE IF (TEST_NAME == "fiat_test_mpl_allocator_init_size_BUDDY_ALLOC") THEN
            CALL TEST_INIT_SIZE(RESOURCE="BUDDY_ALLOC")
        ELSE IF (TEST_NAME == "fiat_test_mpl_allocator_init_size_MPI_POOL") THEN
            CALL TEST_INIT_SIZE(RESOURCE="MPI_POOL")
        ELSE IF (TEST_NAME == "fiat_test_mpl_allocator_init_and_reserve") THEN
            CALL TEST_INIT_AND_RESERVE()
        ELSE IF (TEST_NAME == "fiat_test_mpl_allocator_init_and_reserve_FORTRAN") THEN
            CALL TEST_INIT_AND_RESERVE(RESOURCE="FORTRAN")
        ELSE IF (TEST_NAME == "fiat_test_mpl_allocator_init_and_reserve_BUDDY_ALLOC") THEN
            CALL TEST_INIT_AND_RESERVE(RESOURCE="BUDDY_ALLOC")
        ELSE IF (TEST_NAME == "fiat_test_mpl_allocator_init_and_reserve_MPI_POOL") THEN
            CALL TEST_INIT_AND_RESERVE(RESOURCE="MPI_POOL")
        ELSE
            CALL FAIL("Unrecognized testname "//TRIM(TEST_NAME))
        ENDIF
    ENDIF
ELSE
    CALL FAIL("No test name provided as argument")
ENDIF

if (MPL()) CALL MPL_END(LDMEMINFO=.FALSE.)

CONTAINS

FUNCTION MPL() RESULT(LMPL)
    LOGICAL :: LMPL
    CHARACTER(LEN=512) :: ENV
    CALL GET_ENVIRONMENT_VARIABLE("MPL",ENV)
    IF( ENV == '0' ) THEN
        LMPL = .FALSE.
    ELSE
        LMPL = .TRUE.
    ENDIF
END FUNCTION

END PROGRAM TEST_MPL_ALLOCATOR_DRIVER
