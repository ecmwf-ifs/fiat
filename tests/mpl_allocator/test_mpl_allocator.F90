MODULE TEST_MPL_ALLOCATOR
USE EC_PARKIND, ONLY: JPIM, JPIB, JPRD, JPRM
USE MPL_ALLOCATOR_MOD, ONLY : MPL_ALLOCATOR_INIT, MPL_ALLOCATOR_FINAL, MPL_ALLOCATOR_INITIALIZED, &
                            & MPL_ALLOCATE, MPL_DEALLOCATE, MPL_RESOURCE, &
                            & MPL_ALLOCATOR_RESERVE, MPL_ALLOCATOR_CAPACITY, MPL_ALLOCATOR_ALLOCATED
IMPLICIT NONE
PUBLIC
CONTAINS

SUBROUTINE FAIL(MSG)
    USE MPL_ABORT_MOD, ONLY : MPL_ABORT
    CHARACTER(LEN=*), INTENT(IN) :: MSG
    CALL MPL_ABORT(MSG)
END SUBROUTINE

SUBROUTINE WARN(MSG)
    USE, INTRINSIC :: ISO_FORTRAN_ENV, ONLY : ERROR_UNIT
    CHARACTER(LEN=*), INTENT(IN) :: MSG
    WRITE(ERROR_UNIT,'(A,A)') "WARNING: ",MSG
END SUBROUTINE

FUNCTION GET_EXPECTED_RESOURCE_FROM_ENV() RESULT(CMPL_RESOURCE)
    CHARACTER(LEN=512) :: CMPL_RESOURCE
    CALL GET_ENVIRONMENT_VARIABLE("EXPECTED_MPL_RESOURCE",CMPL_RESOURCE)
END FUNCTION

SUBROUTINE CHECK_MPL_RESOURCE_IS_EXPECTED(EXPECTED_RESOURCE)
    IMPLICIT NONE
    CHARACTER(LEN=*), INTENT(IN), OPTIONAL :: EXPECTED_RESOURCE
    CHARACTER(LEN=256) :: CLEXPECTED_RESOURCE
    IF (PRESENT(EXPECTED_RESOURCE)) THEN
        CLEXPECTED_RESOURCE = EXPECTED_RESOURCE
    ELSE
        CLEXPECTED_RESOURCE = GET_EXPECTED_RESOURCE_FROM_ENV()
    ENDIF
    IF (LEN_TRIM(CLEXPECTED_RESOURCE) > 0) THEN
        IF (MPL_RESOURCE() /= CLEXPECTED_RESOURCE) THEN
            CALL FAIL("Expected: MPL_RESOURCE() == "//TRIM(CLEXPECTED_RESOURCE)//" whereas MPL_RESOURCE() == "&
                     &//TRIM(MPL_RESOURCE()))
        ENDIF
    ENDIF
END SUBROUTINE

SUBROUTINE CHECK_MPL_ALLOCATOR_IS_INITIALIZED(EXPECTED)
    IMPLICIT NONE
    LOGICAL, INTENT(IN) :: EXPECTED
    IF (MPL_ALLOCATOR_INITIALIZED() .NEQV. EXPECTED) THEN
        IF (EXPECTED) THEN
            CALL FAIL("Expected: MPL_ALLOCATOR_INITIALIZED() == .TRUE.")
        ELSE
            CALL FAIL("Expected: MPL_ALLOCATOR_INITIALIZED() == .FALSE.")
        ENDIF
    ENDIF
END SUBROUTINE

