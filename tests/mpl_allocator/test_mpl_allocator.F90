    CALL MPL_DEALLOCATE("ARRAY_JPRM_2D", ARRAY_JPRM_2D)
    CALL MPL_DEALLOCATE("ARRAY_JPIM_2D", ARRAY_JPIM_2D)
    CALL MPL_DEALLOCATE("ARRAY_JPIB_2D", ARRAY_JPIB_2D)

    CALL MPL_DEALLOCATE("ARRAY_JPRD_3D", ARRAY_JPRD_3D)
    CALL MPL_DEALLOCATE("ARRAY_JPRM_3D", ARRAY_JPRM_3D)
    CALL MPL_DEALLOCATE("ARRAY_JPIM_3D", ARRAY_JPIM_3D)
    CALL MPL_DEALLOCATE("ARRAY_JPIB_3D", ARRAY_JPIB_3D)

    CALL MPL_DEALLOCATE("ARRAY_JPRD_4D", ARRAY_JPRD_4D)
    CALL MPL_DEALLOCATE("ARRAY_JPRM_4D", ARRAY_JPRM_4D)
    CALL MPL_DEALLOCATE("ARRAY_JPIM_4D", ARRAY_JPIM_4D)
